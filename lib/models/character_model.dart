class CharacterModel {
  final String? imageUrl;
  final String? name;
  final String? isAlive;
  final String? species;
  final String? gender;
  final String? description;
  final String? placeOfBirth;
  final String? location;

  CharacterModel({
    this.imageUrl,
    this.name,
    this.isAlive,
    this.species,
    this.gender,
    this.description,
    this.placeOfBirth,
    this.location,
  });
}

